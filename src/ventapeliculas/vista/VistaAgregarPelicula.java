    private void jButtonIngresarPeliculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIngresarPeliculaActionPerformed
        // TODO add your handling code here:
          if (jTextFieldNombrePelicula.getText().length() < 3) {
            JOptionPane.showMessageDialog(null, "Ingrese 3 o más caracteres en el nombre de la película");
        } else {
            String nombre = jTextFieldNombrePelicula.getText();
            if (((isNumber(jTextFieldPrecio.getText()) == false)) || 
                   (Integer.parseInt(jTextFieldPrecio.getText()) < 1000)) {
                JOptionPane.showMessageDialog(null, "Ingrese un precio mayor a 1000");
            } else {
                Integer precio = new Integer(jTextFieldPrecio.getText());
            if (jComboBoxCategoria.getSelectedItem()==".."){
                JOptionPane.showMessageDialog(null, "Ingrese una categoría existente");
            }
            else{String categoria = (String) jComboBoxCategoria.getSelectedItem();
                String formato = (String) jComboBoxFormato4k.getSelectedItem();
                String formato4k;
                if ("SI".equals(formato)) {
                    formato4k = "S";
                } else {
                    formato4k = "N";
                }

                ControladorAgregar controladorAgregar = new ControladorAgregar();
                try {
                    controladorAgregar.agregarPelicula(precio, categoria, formato4k, nombre);
                } catch (SQLException ex) {
                    Logger.getLogger(VistaAgregarPelicula.class.getName()).log(Level.SEVERE, null, ex);
                }

                JOptionPane.showMessageDialog(null, "Ingresado Correcto");

            }
        }}
    }//GEN-LAST:event_jButtonIngresarPeliculaActionPerformed

