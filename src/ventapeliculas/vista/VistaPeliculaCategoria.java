    private void jButtonLIstarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLIstarActionPerformed
         try {
             // TODO add your handling code here:
             Listar();
     
             jTable1.setModel(defaultTableModel);
             controladorListar = new ControladorListar();
             
             ArrayList<Pelicula> listPelicula=controladorListar.getListadoPeliculaCategoria((String)jComboBoxCategoria.getSelectedItem());
          
             Object[] columna=new Object[5];
             for(int x=0; x<listPelicula.size(); x++){
                 columna[0]=listPelicula.get(x).getCodigo();
                 columna[1]=listPelicula.get(x).getNombre();
                 columna[2]=listPelicula.get(x).getPrecio();
                 columna[3]=listPelicula.get(x).getFormato4k();
                 columna[4]=listPelicula.get(x).getIdCatalogo();
                 
                 defaultTableModel.addRow(columna);
             }} catch (SQLException ex) {
             Logger.getLogger(VistaPeliculaCategoria.class.getName()).log(Level.SEVERE, null, ex);
         }
    }//GEN-LAST:event_jButtonLIstarActionPerformed

