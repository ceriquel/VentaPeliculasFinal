    public void ComboBox(){
         ControladorListar controladorListar;
        controladorListar = new ControladorListar();
        ArrayList<Categoria> listCategoria=null;
        try {
            listCategoria = controladorListar.getListadoCategoria();
            } catch (SQLException ex) {
                Logger.getLogger(VistaAgregarPelicula.class.getName()).log(Level.SEVERE, null, ex);
            }
            Object[] columna1=new Object[1];
            for(int x=0; x<listCategoria.size(); x++){
                columna1[0]=listCategoria.get(x).getNombreCategoria();
                jComboBoxCategoria.addItem((String)columna1[0]);
            }
    }

